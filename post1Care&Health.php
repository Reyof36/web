  <h2> How to Give a Cat a Bath </h2>
  <img src="style/catBath.jpg" alt="Cat Bath" width="700" height="400">

  <p>
    <h4>Are you Supposed to Bath a Cat?</h4>
    <p> Not all cats hate water. Most of the wild cat species like jaguars and tigers
        actually love taking a deep once in a while.<br>
   <br> Unfortunately, domestic cats like our lovable pets do not take water too kindly.
        The simple explanation is because the coat of a cat does not deflect moisture, instead,
        it gets soaked which can be uncomfortable for the cat and makes it harder for it to get dry.
        Nonetheless, their hatred is not strong enough to make it impossible to bath the cat.</p>

    <br>
    <h4>When to Bath Cat?</h4>
    <p>There is no specific time that is ideal for giving your cat a shower. However, it is better to wash
       the cat when the weather is warm. It makes it easier for the cat to dry. Other than that, there are
       no other preferences on when to wash the cat. If you have a blow dryer, even washing it a night and
       using the dryer (which might scare the cat initially) to dry the cat can work.</p>

    <br>
    <h4>What Do You Need?</h4>
    <p>Before you get started, you need to make sure that you have all supplies ready before you actually
       soak the cat in the water. They are not patient animals and their hate for water is well documented.
       If you were to forget something in the other room, you would be at pains thinking of whether to do
       without it or go get it and have to run after your wet cat after.<br>
   <br>Some of the equipment you will need when learning how to give a cat a bath includes; <br>
    <ul>
      <li>Shampoo for your cat.</li>
      <li>A large towel.</li>
      <li>A small cloth for cleaning the face.</li>
      <li>Cotton balls to clean the ears.</li>
      <li>A gentle spray nozzle or a large pitcher for rinsing.</li>
      <li>Rubber gloves.</li>
    </ul></p>

    <br>
    <h4>How to Give a Cat a Bath</h4>
    <p>It is better to bath the cat in a kitchen or bathroom sink as opposed to bending over a tub.
       With a sink, you will have just enough space to bath the cat and not too much that he will cause
       trouble. For the best results, you might want to follow this step by step procedure. Before you get
       the cat wet, make sure you brush it to get off the mats.
    <ul>
      <li>After you have prepared everything you need to bath the cat, fill the sink with about three inches
          of lukewarm water.</li>
      <li>Take the cat and wet it from the shoulders down to the tail then apply the shampoo.</li>
      <li>Once you have applied a generous amount of the shampoo, work it in the same way you would if it
          was your hair.</li>
      <li>Rinse the cat.</li>
      <li>Because cats are very sensitive about having water on their face, it is best to use a small dump
          towel to clean the cats head and face you can also use the cotton balls to clean the cat’s ears.
          You should never insert anything into the cat’s ear.</li>
      <li>Once you have thoroughly rinsed the cat and you have cleaned the face, head and ears, place the cat
          in the towel and rub as much as water as possible.</li>
      <li>If your cat is a long-haired breed, then you can use a blow dryer to dry the cat. Some cats will be
          terrified by the noise. If this is the case with your cat, then you’re better off leaving him alone
          and using the towel to suck up as much water as possible.</li>
    </ul> </p> <br>

    <h4>What if I can’t Bath my Cat?</h4>
    <p>Some cats can be feisty especially if this is her first time taking a shower. If she becomes difficult
       to handle and a shower is necessary, the best approach would be to get in touch with your vet. With
       the experience that they have in handling animals, they are better equipped to do the cleaning for you.<br>
    <br> With time, the pet will get more accustomed and become more relaxed.</p>

