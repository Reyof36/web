  <h2>Why Does My Cat Bite Me Gently? </h2>
  <img src="style/catBite2.jpg" alt="cat Bite" width="700" height="400">
  <p>
      <p>There are different meanings for when a cat bites you. Cats normally bite gently to communicate
        something to you. Therefore, you can to consider your cat's body language every time it bites you.
        For instance, you have to know what is happening in their "world" to provide you with information on
        what they are trying to communicate. Here are a few reasons that will help you find an answer to the
        question why does my cat bite me gently? </p> <br>

      <h4> Affection </h4>
      <p> At times, the nibbling and biting may be a way for your cat to show you love. This behavior may at
          times be annoying. However, even as you give him some treats to avert his attention or give him a toy,
          consider the fact that one of the reasons your cat bites you is to show affection.<br>
      <br>Therefore, never hit, or yell at your cat to make the cat stop this behavior. Apart from giving treats
          or providing a toy to play with, just walk away anytime this behavior begins. </p>

      <br>
      <h4> Play </h4>
      <p> When a cat wants to play with you, it may use gentle biting as a way of prodding you to play with him.
          If you happen to have an adult cat that bites roughly while playing, discipline the cat by walking
         away every time he does so. Eventually, your cat will get the idea, avoid rough play, and bite hard.</p>

      <br>
      <h4> Stress </h4>
      <p> For some cats, the biting and licking often go further to become suckling. This means that the kitten
          was separate from the littermates and mother too early.<br>
     <br> Essentially, suckling after biting intensifies at times when a cat feels insecure or stressed.
          If you do not mind this behavior, it is okay. However, if you do not like and it annoys you, you
          can simply give him a treat, toy, or walk away. A cat pheromone product may help reduce the anxiety
          that your cat feels.</p>

      <br>
      <h4>Overstimulation</h4>
      <p> Most cats tend to get over stimulated every time you play with it for too long. When this happens,
          you will see an angry look in his face and then he may proceed to bite you.
          The issue here is that you cannot train your cat to stop being over stimulated; too much play will
          definitely lead to a few bites on your arms to try to show you that he is agitated.<br>
      <br> Here are some of the signs that your cat is getting angry and is likely to start biting you.
        <ul>
          <li>Your cats tail is wagging in a slow movement.</li>
          <li>Your cat's ears stiffen backward.</li>
          <li>Your catâ€™s body starts to stiffen.</li>
        </ul> <br></p>
      <p> These are signs of aggression, therefore; it is time to slow down the play. You can choose to walk
            away and let the cat calm down. Also, learn how to time how long it takes your cat to get over stimulated.
            There are some cats that will move from purring to cuddling, to over stimulation in seconds. Therefore,
            learn how to read the above signals and the duration it takes to over stimulate your cat to the point where
            he bites you.</p>

