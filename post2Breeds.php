  <h2>  5 cat breeds with the friendliest personalities </h2>
  <br>
  <h4> 1. Maine Coon </h4>
  <img src="style/mainCoon.jpg" alt="main coon cat" width="700" height="400">

  <p> There’s a reason these cats have the nickname “gentle giants.”<br>
      While larger than the average cat – they usually range in size from nine to 18 pounds.
      Their meow is a high-pitched, light sound, often surprising to hear coming from such
      a massive fur ball.<br>
<br> These friendly cat breeds also generally get along great with humans and other cats alike,
     making them ideal choices for multi-pet households.</p>

<br>

 <h4> 2.Burmese </h4>
 <img src="style/Burmese.jpg" alt="Burmese Cat" width="700" height="400">
 <p> A majestic-looking feline originally from southeast Asia, the Burmese is a great choice
     if you want a cat that’ll cuddle up in your lap.<br>

<br> Relatively small in size but often stocky, these friendly cat breeds love curling up for a snuggle,
     and they have no problem letting their humans know when
     they want attention.</p>

<br>

<h4> 3.Persian</h4>
<img src="style/Persian.jpg" alt="Persian cat" width="700" height="400">
<p> These fluffy white felines might be the chosen companions of Bond villains, and while it is true that
    Persians generally love being held and pampered, the bad-guy association
    doesn’t hold up.<br>
<br>While they love a good cuddle, they tend not to be particularly rambunctious or playful.<br>

<br>But while they may not make the best choice for people looking for a peppy pet, they’re an affectionate
    companion for those seeking something a little more laid-back.</p>

<br>

<h4>4.Exotic Shorthair </h4>
<img src="style/ExoticShorthair.jpg" alt="Exotic Shorthair Cat" width="700" height="400">
<p> Exotic Shorthairs are directly related to Persians – they were bred to be a mix of Persians and American
    Shorthairs.<br>

<br>As such, they’re similar to Persians in both looks and personality. Since their short coats require
    significantly less grooming than Persians’ ample fur, they’re often touted
    as ideal alternatives to Persians for people with busier schedules.</p>

<br>

<h4>5.Ragdoll</h4>
<img src="style/Ragdoll.jpg" alt="Ragdoll Cat" width="700" height="400">
<p> Most cat people know exactly how the Ragdoll cat got its name:
  When a human picks it up, it’ll go limp like a rag doll.<br>

<br>But there’s far more to Ragdolls than just passive balls of fur – these friendly cat breeds love to
    play with their trusted humans and have been known
    to even come when they’re called.<br>

<br>They’re super affectionate and love to follow their people around. </p>

