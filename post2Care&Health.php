  <h2>How To Tell If a Cat Has a Broken Leg?</h2>
  <img src="style/brokenCat.jpeg" alt="Broken Cat" width="700" height="400">
  <p> <p>Cats are naturally adventurous and sometimes they can hurt themselves unwittingly
         when they are out exploring, or perhaps some cruel person hurt our cat and we weren’t
         there to witness it. No matter the reason, here are ways on how to tell if a cat has
         a broken leg!</p>
      <br>
      <h4>Here are the telltale signs!</h4>
      <br>
      <h4>1.Lethargy.</h4>
      <p>Sometimes a cat would refuse to do anything at all. They would look like they’re out of
         energy, lazy and would rather sleep a lot. It would be difficult to tell at this point, but
         this is usually an indicator that something might be wrong.</p>
      <br>
      <h4>2.Odd way of walking.</h4>
      <p>Your cat might be walking out of the ordinary, or maybe not walking at all. Watch out for any
         signs of limping and hesitation to put on weight in a certain area. Most likely, this is the
         affected area.</p>
      <br>
      <h4>3.Untoward reactions when the affected area is touched.</h4>
      <p>When your cat flinches and hisses at you when you pet them on certain areas, it could mean
        that they are in pain. They could be feeling tenderness around the area, so if you find
        a particular area hurts, avoid touching them any further.</p>
      <br>
      <h4>4.Signs of shock.</h4>
      <p>If the injury just happened, sometimes your cat will appear to be in shock, and might not be
         able to move at all. See also if your cat’s breath suddenly hitched.</p>
      <br>
      <h4>5.Physical appearance.</h4>
      <p>Probably the most obvious way on how to tell if a cat has a broken leg. There is an evident
        swelling around the injured leg. A deformity around the area is seen – a bent limb or a bone
        sticking out through the skin. Sometimes cats endure walking despite a bent limb, but cat
        x-rays will surely tell you that there is something wrong.</p>
      <br>
      <br>
      <h4>Administering first-aid.</h4>
      <p>While some say that cats have the ability to heal themselves, it would help if you would
         know how to give your cat first-aid. This will somehow lessen the pain they are feeling.
         Keep your cat well fed and nourished as they will need the nutrients when they heal.<br>
      <br>Observe your cat if these symptoms persist. Consult with your veterinarian right away
      if necessary. </p>
